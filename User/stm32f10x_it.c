void USART1_IRQHandler(void)                //串口1中断服务程序
{
 
	uint8_t Usart1_Rec_Cnt;
  int i;
     if(USART_GetITStatus(DEBUG_USARTx, USART_IT_IDLE) != RESET) //接收中断(接收到的数据必须是0x0d 0x0a结尾)
      {
          USART_ReceiveData(DEBUG_USARTx);//读取数据注意：这句必须要，否则不能够清除中断标志位。
          Usart1_Rec_Cnt = RECEIVEBUFF_SIZE - DMA_GetCurrDataCounter(USART_RX_DMA_CHANNEL); //算出接本帧数据长度
 
         //***********帧数据处理函数(调试使用)************//
          printf ("Data lenght:%d\r\n",Usart1_Rec_Cnt);
          printf ("The data:");
				  for(i=0;i<Usart1_Rec_Cnt;i++) Usart_SendByte(DEBUG_USARTx,ReceiveBuff[i]);
				  printf ("\r\n");
        //*************************************//
         USART_ClearITPendingBit(DEBUG_USARTx,USART_IT_IDLE);         //清除中断标志
         MYDMA_Enable(USART_RX_DMA_CHANNEL);                  //恢复DMA指针，等待下一次的接收
     }
 
}
